#include <stdio.h>
#include <stdbool.h>

// In C boolean values are noted as 1s and 0s, 1 == true and 0 == false
int main()
{
	bool isProgrammingFun = true;
	bool isTomatoTasty = false;

	// Return boolean values
	printf("%d", isProgrammingFun);   // Returns 1 (true)
	printf("%d", isTomatoTasty);        // Returns 0 (false)
}
